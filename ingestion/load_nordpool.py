ZONES = ["SE1", "SE2", "SE3", "SE4"]


def parse_rows() -> list[tuple]:
    rows = []
    for line in RAW_DATA.strip().splitlines():
        parts = line.strip().split(",")
        d = parts[0]
        for i, zone in enumerate(ZONES):
            price = float(parts[i + 1])
            rows.append((zone, f"{d} 12:00:00", price))
    return rows


if __name__ == "__main__":
    rows = parse_rows()
    conn = get_connection()
    cur = conn.cursor()

    cur.execute("""
        DELETE FROM RAW_DB.ENERGY.HOURLY_PRICES
        WHERE DATE(timestamp_utc) >= '2026-01-01'
        AND DATE(timestamp_utc) <= '2026-05-13'
    """)

    cur.executemany("""
        INSERT INTO RAW_DB.ENERGY.HOURLY_PRICES (bidding_zone, timestamp_utc, price_eur_mwh)
        VALUES (%s, %s, %s)
    """, rows)

    conn.commit()
    conn.close()
    print(f"Inserted {len(rows)} energy rows.")
